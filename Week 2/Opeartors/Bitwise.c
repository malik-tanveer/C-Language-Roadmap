#include <stdio.h>

int main()
{
    // Bitwise Opearotrs


    // Bitwise Operators: Perform operations on the individual bits of integer data types.
    // & (Bitwise AND)
    // | (Bitwise OR)
    // ^ (Bitwise XOR)
    // ~ (Bitwise NOT/Complement)
    // << (Left shift)
    // >> (Right shift)

    int a = 10;
    int b = 10;

    printf("a & b = %d\n", a & b);
    printf("a | b = %d\n", a | b);
    printf("a ^ b = %d\n", a ^ b);
    printf("~b = %d\n", ~b);
    printf("a << b = %d\n", a << b);
    printf("a >> b = %d\n", a >> b);


    return 0;
}