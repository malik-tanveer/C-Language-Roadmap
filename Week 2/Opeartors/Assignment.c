#include <stdio.h>

int main(){
    // Learn a Assigment opearotrs

    // = (Simple assignment)
    // +=, -=, *=, /=, %= (Combine an arithmetic operation with assignment,

    // this is a most use case for loop and while loop is a most use case is a easily understand and assign a value is easily and simple  you not make a second variable 

    int x = 10;

    // Addition assignment	x += 5;	x = x + 5;
    printf("x += 5 = %d\n", x += 5);
    
    // 	Subtraction assignment	x -= 3;	x = x - 3;
    printf("x -= 12 = %d\n", x -= 12);
    
    // 	Multiplication assignment	x *= 4;	x = x * 4;
    printf("x *= 4 = %d\n", x *= 4);
    
    // 	Division assignment	x /= 2;	x = x / 2;
    printf("x *= 2 = %d\n", x *= 2);
    
    // Modulus assignment	x %= 3;	x = x % 3;
    printf("x *= 3 = %d", x *= 3);
    
    return 0;
}